function plus(numberA, numberB){
    return numberA + numberB;
}

function minus(numberA, numberB){
    return numberA - numberB;
}

function times(numberA, numberB){
    return numberA * numberB;
}

function div(numberA, numberB){
    return numberA / numberB;
}

function mod(numberA, numberB){
    return numberA % numberB;  
}
